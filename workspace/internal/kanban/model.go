// ── State machine ─────────────────────────────────────────────────────────────

// validTransitions defines allowed vehicle state transitions.
var validTransitions = map[string][]string{
	"sourcing":       {"acquired"},
	"acquired":       {"reconditioning", "listed"},
	"reconditioning": {"photography", "listed"},
	"photography":    {"listed"},
	"listed":         {"inquiry", "reserved", "sold"},
	"inquiry":        {"negotiation", "listed"},
	"negotiation":    {"reserved", "inquiry"},
	"reserved":       {"sold", "negotiation"},
	"sold":           {"in_transit"},
	"in_transit":     {"delivered"},
	"delivered":      {},
}

// ValidateTransition returns an error if from→to is not allowed.
func ValidateTransition(from, to string) error {
	allowed, ok := validTransitions[from]
	if !ok {
		return fmt.Errorf("unknown state %q", from)
	}
	for _, a := range allowed {
		if a == to {
			return nil
		}
	}
	return fmt.Errorf("transition %q → %q is not allowed", from, to)
}

