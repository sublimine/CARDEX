// ── Kanban types ──────────────────────────────────────────────────────────────

// Column is a Kanban board column (maps 1:1 to a vehicle state).
type Column struct {
	ID           string `json:"id"`
	TenantID     string `json:"tenant_id"`
	Name         string `json:"name"`
	Position     int    `json:"position"`
	Color        string `json:"color"`
	VehicleLimit int    `json:"vehicle_limit"`
	IsDefault    bool   `json:"is_default"`
	StateKey     string `json:"state_key,omitempty"`
	CreatedAt    string `json:"created_at"`
	UpdatedAt    string `json:"updated_at"`
	Cards        []Card `json:"cards,omitempty"`
}

// Card is a vehicle's representation on the board.
type Card struct {
	VehicleID  string   `json:"vehicle_id"`
	TenantID   string   `json:"tenant_id"`
	ColumnID   string   `json:"column_id"`
	Position   int      `json:"position"`
	AssigneeID string   `json:"assignee_id,omitempty"`
	Priority   string   `json:"priority"`
	Labels     []string `json:"labels"`
	DueDate    string   `json:"due_date,omitempty"`
	UpdatedAt  string   `json:"updated_at"`
}

// MoveRequest is the body for PUT /kanban/cards/{vehicleId}/move.
type MoveRequest struct {
	TargetColumnID string `json:"target_column_id"`
	Position       int    `json:"position"`
}

// CardPatch is the body for PUT /kanban/cards/{vehicleId}.
type CardPatch struct {
	AssigneeID *string  `json:"assignee_id,omitempty"`
	Priority   *string  `json:"priority,omitempty"`
	Labels     []string `json:"labels,omitempty"`
	DueDate    *string  `json:"due_date,omitempty"`
}

// ColumnPatch is the body for PUT /kanban/columns/{id}.
type ColumnPatch struct {
	Name         *string `json:"name,omitempty"`
	Color        *string `json:"color,omitempty"`
	VehicleLimit *int    `json:"vehicle_limit,omitempty"`
}

