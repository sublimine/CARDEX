func newStatsCmd() *cobra.Command {
	return &cobra.Command{
		Use:   "stats",
		Short: "Show aggregate pipeline statistics",
		RunE: func(_ *cobra.Command, _ []string) error {
			db, err := openDB(flagDBPath)
			if err != nil {
				return err
			}
			defer db.Close()
			return runStats(db)
		},
	}
}

func runStats(db *sql.DB) error {
	type countryStat struct {
		country string
		count   int
		avgKm   float64
		avgEUR  float64
	}

	rows, err := db.Query(`
		SELECT
			COALESCE(de.country_code, 'UNKNOWN') AS country,
			COUNT(*) AS cnt,
			AVG(COALESCE(vr.mileage_km, 0)) AS avg_km,
			AVG(COALESCE(vr.price_gross_eur, 0)) AS avg_eur
		FROM vehicle_record vr
		LEFT JOIN dealer_entity de ON de.dealer_id = vr.dealer_id
		GROUP BY country
		ORDER BY cnt DESC`)
	if err != nil {
		return fmt.Errorf("stats: %w", err)
	}
	defer rows.Close()

	var stats []countryStat
	var total int
	for rows.Next() {
		var s countryStat
		if err := rows.Scan(&s.country, &s.count, &s.avgKm, &s.avgEUR); err != nil {
			return err
		}
		stats = append(stats, s)
		total += s.count
	}
	if err := rows.Err(); err != nil {
		return err
	}

	var dealers int
	db.QueryRow(`SELECT COUNT(*) FROM dealer_entity WHERE status='ACTIVE'`).Scan(&dealers) //nolint:errcheck

	var avgScore float64
	db.QueryRow(`SELECT AVG(confidence_score) FROM vehicle_record WHERE confidence_score > 0`).Scan(&avgScore) //nolint:errcheck

	fmt.Println()
	fmt.Println(styleHeader.Render("CARDEX PIPELINE STATS"))
	fmt.Printf("  %s %s\n", styleBold.Render("Generated:"), time.Now().Format("2006-01-02 15:04:05 MST"))
	fmt.Println()
	fmt.Printf("  %-20s %s\n", styleBold.Render("Active dealers:"), styleGreen.Render(strconv.Itoa(dealers)))
	fmt.Printf("  %-20s %s\n", styleBold.Render("Total vehicles:"), styleGreen.Render(strconv.Itoa(total)))
	fmt.Printf("  %-20s %s\n", styleBold.Render("Avg quality score:"), scoreStyle(avgScore*100))
	fmt.Println()

	if len(stats) > 0 {
		fmt.Println(styleHeader.Render("BY COUNTRY"))
		fmt.Printf("  %-8s %8s %10s %12s\n",
			styleBold.Render("COUNTRY"),
			styleBold.Render("VEHICLES"),
			styleBold.Render("AVG KM"),
			styleBold.Render("AVG PRICE€"),
		)
		fmt.Println("  " + strings.Repeat("─", 44))
		for _, s := range stats {
			fmt.Printf("  %-8s %8d %10.0f %12.0f\n",
				s.country, s.count, s.avgKm, s.avgEUR)
		}
	}
	fmt.Println()
	return nil
}

// ── Formatting helpers ────────────────────────────────────────────────────────

func field(label, value string) string {
	return fmt.Sprintf("  %-18s %s\n", styleDim.Render(label+":"), value)
}

func scoreStyle(score float64) string {
	s := fmt.Sprintf("%5.1f%%", score)
	switch {
	case score >= 80:
		return styleGreen.Render(s)
	case score >= 60:
		return styleYellow.Render(s)
	default:
		return styleRed.Render(s)
	}
}

