// ── cardex search ─────────────────────────────────────────────────────────────

func newSearchCmd() *cobra.Command {
	cmd := &cobra.Command{
		Use:   "search",
		Short: "Search vehicle listings",
		Example: `  cardex search --country DE --make BMW --model "Serie 3" --year-min 2020 --price-max 25000
  cardex search --make Renault --km-max 80000 --score 70`,
		RunE: func(cmd *cobra.Command, _ []string) error {
			db, err := openDB(flagDBPath)
			if err != nil {
				return err
			}
			defer db.Close()
			return runSearch(db)
		},
	}
	cmd.Flags().StringVar(&flagCountry, "country", "", "ISO-3166-1 country code (DE, FR, ES, NL, BE, CH)")
	cmd.Flags().StringVar(&flagMake, "make", "", "vehicle make (case-insensitive)")
	cmd.Flags().StringVar(&flagModel, "model", "", "vehicle model (case-insensitive, partial match)")
	cmd.Flags().IntVar(&flagYearMin, "year-min", 0, "minimum model year (inclusive)")
	cmd.Flags().IntVar(&flagYearMax, "year-max", 0, "maximum model year (inclusive)")
	cmd.Flags().IntVar(&flagPriceMin, "price-min", 0, "minimum price in EUR")
	cmd.Flags().IntVar(&flagPriceMax, "price-max", 0, "maximum price in EUR")
	cmd.Flags().IntVar(&flagKmMax, "km-max", 0, "maximum odometer reading in km")
	cmd.Flags().Float64Var(&flagScore, "score", 0, "minimum composite quality score (0–100)")
	cmd.Flags().IntVar(&flagLimit, "limit", 25, "results per page")
	cmd.Flags().IntVar(&flagPage, "page", 1, "page number (1-based)")
	return cmd
}

func runSearch(db *sql.DB) error {
	query, args := buildSearchQuery()
	rows, err := db.Query(query, args...)
	if err != nil {
		return fmt.Errorf("search: %w", err)
	}
	defer rows.Close()

	type row struct {
		id, make_, model, vin, country string
		year, mileage, priceNet        int
		score                          float64
		url                            string
	}

	var results []row
	for rows.Next() {
		var r row
		var priceNet sql.NullInt64
		var score sql.NullFloat64
		var url sql.NullString
		if err := rows.Scan(&r.id, &r.make_, &r.model, &r.vin, &r.country,
			&r.year, &r.mileage, &priceNet, &score, &url); err != nil {
			return err
		}
		r.priceNet = int(priceNet.Int64)
		r.score = score.Float64
		r.url = url.String
		results = append(results, r)
	}
	if err := rows.Err(); err != nil {
		return err
	}

	if len(results) == 0 {
		fmt.Println(styleDim.Render("No listings match your filters."))
		return nil
	}

	// ── Header row ──
	hdr := fmt.Sprintf("%-26s %-12s %-18s %5s %8s %8s %6s  %s",
		"ID", "COUNTRY/MAKE", "MODEL", "YEAR", "KM", "PRICE€", "SCORE", "URL")
	fmt.Println(styleHeader.Render(hdr))
	fmt.Println(strings.Repeat("─", 100))

	for _, r := range results {
		scoreStr := scoreStyle(r.score)
		priceStr := fmt.Sprintf("%8d", r.priceNet)
		urlShort := r.url
		if len(urlShort) > 40 {
			urlShort = urlShort[:37] + "..."
		}
		line := fmt.Sprintf("%-26s %-12s %-18s %5d %8d %s %s  %s",
			r.id, r.country+"/"+r.make_, r.model,
			r.year, r.mileage, priceStr, scoreStr, styleDim.Render(urlShort))
		fmt.Println(line)
	}
	fmt.Printf("\n%s\n", styleDim.Render(fmt.Sprintf("Page %d — %d result(s)  (--page / --limit to paginate)", flagPage, len(results))))
	return nil
}

func buildSearchQuery() (string, []any) {
	var conds []string
	var args []any

	if flagCountry != "" {
		conds = append(conds, "UPPER(de.country_code) = UPPER(?)")
		args = append(args, flagCountry)
	}
	if flagMake != "" {
		conds = append(conds, "UPPER(vr.make_canonical) = UPPER(?)")
		args = append(args, flagMake)
	}
	if flagModel != "" {
		conds = append(conds, "UPPER(vr.model_canonical) LIKE UPPER(?)")
		args = append(args, "%"+flagModel+"%")
	}
	if flagYearMin > 0 {
		conds = append(conds, "vr.year >= ?")
		args = append(args, flagYearMin)
	}
	if flagYearMax > 0 {
		conds = append(conds, "vr.year <= ?")
		args = append(args, flagYearMax)
	}
	if flagPriceMin > 0 {
		conds = append(conds, "vr.price_gross_eur >= ?")
		args = append(args, flagPriceMin)
	}
	if flagPriceMax > 0 {
		conds = append(conds, "vr.price_gross_eur <= ?")
		args = append(args, flagPriceMax)
	}
	if flagKmMax > 0 {
		conds = append(conds, "vr.mileage_km <= ?")
		args = append(args, flagKmMax)
	}
	if flagScore > 0 {
		conds = append(conds, "vr.confidence_score >= ?")
		args = append(args, flagScore/100.0)
	}

	where := ""
	if len(conds) > 0 {
		where = "WHERE " + strings.Join(conds, " AND ")
	}

	offset := (flagPage - 1) * flagLimit
	query := fmt.Sprintf(`
		SELECT
			vr.vehicle_id,
			COALESCE(vr.make_canonical, ''),
			COALESCE(vr.model_canonical, ''),
			COALESCE(vr.vin, ''),
			COALESCE(de.country_code, ''),
			COALESCE(vr.year, 0),
			COALESCE(vr.mileage_km, 0),
			CAST(COALESCE(vr.price_gross_eur, 0) AS INTEGER),
			COALESCE(vr.confidence_score, 0.0),
			COALESCE(vr.source_url, '')
		FROM vehicle_record vr
		LEFT JOIN dealer_entity de ON de.dealer_id = vr.dealer_id
		%s
		ORDER BY vr.indexed_at DESC
		LIMIT ? OFFSET ?`, where)

	args = append(args, flagLimit, offset)
	return query, args
}

