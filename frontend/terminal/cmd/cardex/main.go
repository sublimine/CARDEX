// ── Styles ────────────────────────────────────────────────────────────────────

var (
	styleHeader  = lipgloss.NewStyle().Bold(true).Foreground(lipgloss.Color("33"))
	styleBold    = lipgloss.NewStyle().Bold(true)
	styleGreen   = lipgloss.NewStyle().Foreground(lipgloss.Color("82"))
	styleYellow  = lipgloss.NewStyle().Foreground(lipgloss.Color("220"))
	styleRed     = lipgloss.NewStyle().Foreground(lipgloss.Color("196"))
	styleDim     = lipgloss.NewStyle().Faint(true)
	styleBorder  = lipgloss.NewStyle().
			Border(lipgloss.NormalBorder()).
			BorderForeground(lipgloss.Color("240")).
			Padding(0, 1)

	// styleAIBadge renders the "[AI]" disclosure badge required by EU AI Act
	// Art. 50(1) — visible indicator that the description was generated by an LLM.
	styleAIBadge = lipgloss.NewStyle().
			Bold(true).
			Foreground(lipgloss.Color("0")).
			Background(lipgloss.Color("220")).
			Padding(0, 1)
)

// ── DB helpers ────────────────────────────────────────────────────────────────

func openDB(path string) (*sql.DB, error) {
	if path == "" {
		path = defaultDBPath()
	}
	db, err := sql.Open("sqlite", path+"?_journal_mode=WAL&_busy_timeout=5000")
	if err != nil {
		return nil, fmt.Errorf("open db %q: %w", path, err)
	}
	db.SetMaxOpenConns(1)
	return db, nil
}

func defaultDBPath() string {
	if p := os.Getenv("CARDEX_DB_PATH"); p != "" {
		return p
	}
	return filepath.Join(".", "data", "discovery.db")
}

