// TestV11_AIAct_EvidenceFields verifies that evidence fields are populated when
// AI metadata is valid.
func TestV11_AIAct_EvidenceFields(t *testing.T) {
	val := v11_nlg_quality.New()
	desc := "BMW 320d in sehr gutem Zustand mit vollständiger Servicedokumentation. " +
		"Ledersitze, Panoramadach, Navigationssystem. Erstbesitz aus München."
	res, err := val.Validate(context.Background(), aiVehicle(desc, validMeta()))
	if err != nil {
		t.Fatalf("unexpected error: %v", err)
	}
	if res.Evidence["ai_generated_model"] != "llama-3.2-3b-instruct" {
		t.Errorf("expected evidence ai_generated_model=llama-3.2-3b-instruct, got %q",
			res.Evidence["ai_generated_model"])
	}
	if res.Evidence["ai_generated_lang"] != "de" {
		t.Errorf("expected evidence ai_generated_lang=de, got %q", res.Evidence["ai_generated_lang"])
	}
}
